package cdelivery_demo;

public class account {
	
	String account_number;
	String account_name;
	String account_primary_firstname;
	String account_primary_lastname;
	long   account_value;
	
	public String getAccount_number() {
		return account_number;
	}
	public void setAccount_number(String account_number) {
		this.account_number = account_number;
	}
	public String getAccount_name() {
		return account_name;
	}
	public void setAccount_name(String account_name) {
		this.account_name = account_name;
	}
	public String getAccount_primary_firstname() {
		return account_primary_firstname;
	}
	public void setAccount_primary_firstname(String account_primary_firstname) {
		this.account_primary_firstname = account_primary_firstname;
	}
	public String getAccount_primary_lastname() {
		return account_primary_lastname;
	}
	public void setAccount_primary_lastname(String account_primary_lastname) {
		this.account_primary_lastname = account_primary_lastname;
	}
	public long getAccount_value() {
		return account_value;
	}
	public void setAccount_value(long account_value) {
		this.account_value = account_value;
	}
}
